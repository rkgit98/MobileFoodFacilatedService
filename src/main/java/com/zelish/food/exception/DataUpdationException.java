package com.zelish.food.exception;

public class DataUpdationException extends RuntimeException{

	public DataUpdationException() {
		super();
	}

	public DataUpdationException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataUpdationException(String message) {
		super(message);
	}

	public DataUpdationException(Throwable cause) {
		super(cause);
	}
	
	
}
